/*百钱买百鸡，一百个铜钱买一百只鸡 
  其中公鸡一只5钱，母鸡一只3钱，小鸡一钱3只，问各多少？
  解题思路：
  设x,y,z;
  1)1<=x<=20
  2)1<=y<=33
  3)3<=z<=99，步长为3 
*/ 
#include<stdio.h>
int main()
{
	int x,y,z;
	for(x=1;x<=20;x++)
	for(y=1;y<=33;y++)
	for(z=3;z<=99;z+=3)
	if((5*x+y*3+z/3==100)&&(x+y+z==100))
	printf("公鸡：%4d母鸡：%4d小鸡：%4d\n",x,y,z);	
	return 0;
}  
